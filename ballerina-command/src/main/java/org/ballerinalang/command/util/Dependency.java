package org.ballerinalang.command.util;

/**
 * Consist of information.
 */
public class Dependency {
    private String name;

    public Dependency() {
        this.name = "";
    }

    public Dependency(String name) {
        this.name = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
